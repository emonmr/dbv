  `submitTimeUTC` varchar(30) DEFAULT NULL,
  `submitTimeLocal` varchar(30) DEFAULT NULL,
  `transactionType` varchar(50) DEFAULT NULL,
  `transactionStatus` int(50) DEFAULT NULL,
  `responseCode` int(5) DEFAULT NULL,
  `responseReasonCode` int(5) DEFAULT NULL,
  `responseReasonDescription` int(30) DEFAULT NULL,
  `authCode` int(20) DEFAULT NULL,
  `AVSResponse` int(10) DEFAULT NULL,
  `batch_batchId` int(15) DEFAULT NULL,
  `batch_settlementTimeUTC` int(30) DEFAULT NULL,
  `batch_settlementTimeLocal` int(30) DEFAULT NULL,
  `batch_settlementState` int(30) DEFAULT NULL,
  `authAmount` int(20) DEFAULT NULL,
  `settleAmount` int(20) DEFAULT NULL,
  `taxExempt` int(20) DEFAULT NULL,
  `payment_creditCard_cardNumber` int(20) DEFAULT NULL,
  `payment_creditCard_expirationDate` int(15) DEFAULT NULL,
  `payment_creditCard_cardType` int(30) DEFAULT NULL,
  `customer_type` int(20) DEFAULT NULL,
