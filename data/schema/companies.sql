CREATE TABLE `companies` (
  `id` int(3) NOT NULL AUTO_INCREMENT,
  `name` varchar(120) NOT NULL COMMENT 'name of the company',
  `slogan` varchar(120) NOT NULL DEFAULT '-' COMMENT 'slogan of the copany',
  `description` text NOT NULL COMMENT 'description of the company',
  `website` varchar(80) NOT NULL DEFAULT '-' COMMENT 'http://www.oficialwebsite.com',
  `primary_email_address` varchar(80) NOT NULL COMMENT 'primary email address of this company',
  `secondary_email_address` varchar(80) NOT NULL DEFAULT '-' COMMENT 'additional company email address',
  `billing_email_address` varchar(80) NOT NULL DEFAULT '-' COMMENT 'billing department email',
  `shipping_email_address` varchar(80) NOT NULL DEFAULT '-',
  `admin_email_address` varchar(80) NOT NULL DEFAULT '-' COMMENT 'email address for this company admin',
  `primary_street` varchar(80) DEFAULT '-' COMMENT 'primary street address of this customer',
  `secondary_street` varchar(80) DEFAULT '-' COMMENT 'secondary street',
  `city` varchar(60) NOT NULL DEFAULT '-' COMMENT 'company city',
  `state` varchar(60) NOT NULL DEFAULT '-' COMMENT 'state of the company address',
  `postal_code` int(5) unsigned zerofill NOT NULL DEFAULT '00000' COMMENT 'postal code of the company',
  `primary_phone` varchar(16) NOT NULL DEFAULT '-' COMMENT 'primary phone or mobile number',
  `secondary_phone` varchar(16) NOT NULL DEFAULT '-' COMMENT 'secondary phone or mobile number',
  `crm_api_url` varchar(80) NOT NULL COMMENT 'CRM API connect URL',
  `crm_api_username` varchar(40) NOT NULL COMMENT 'this company''s CRM API username',
  `crm_api_password` varchar(40) NOT NULL COMMENT 'CRM API Company''s password',
  `crm_db_host` varchar(80) NOT NULL COMMENT 'CRM DB Host',
  `crm_db_username` varchar(40) NOT NULL COMMENT 'CRM DB Username',
  `crm_db_password` varchar(40) NOT NULL COMMENT 'CRM DB Password',
  `crm_db_database` varchar(60) NOT NULL COMMENT 'CRM DB Database',
  `crm_customer_sync_status` text COMMENT 'all sync status and configuration will be kept here',
  `logo_url` text NOT NULL COMMENT 'official company logo url',
  `logo` varchar(200) NOT NULL,
  `authorizenet_api_id` varchar(30) DEFAULT NULL COMMENT 'Authorize.Net payment gateway API ID',
  `authorizenet_access_key` varchar(60) NOT NULL COMMENT 'Authorize.Net Payment gateway access code',
  `authorizenet_sandbox` tinyint(1) NOT NULL DEFAULT '1' COMMENT '1=true=demo gateway, 0=false=payment LIVE',
  `is_visible` tinyint(1) NOT NULL DEFAULT '1' COMMENT 'whole company disable from this application',
  `user_count` int(5) DEFAULT '0' COMMENT 'total user of this company',
  `customer_count` int(8) DEFAULT '0' COMMENT 'total customer of this company',
  `order_count` int(5) DEFAULT '0' COMMENT 'total order for this company',
  `product_count` int(5) NOT NULL DEFAULT '0',
  `status` int(1) NOT NULL DEFAULT '0' COMMENT 'arbitrary use',
  `user_id` int(5) unsigned zerofill NOT NULL COMMENT 'Who added this company to this application',
  `created` datetime NOT NULL,
  `modified` datetime NOT NULL,
  PRIMARY KEY (`id`),
  KEY `user_id` (`user_id`),
  CONSTRAINT `companies_ibfk_1` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8