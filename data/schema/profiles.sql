CREATE TABLE `profiles` (
  `id` int(5) unsigned zerofill NOT NULL AUTO_INCREMENT,
  `user_id` int(5) unsigned zerofill NOT NULL COMMENT 'which user''s profile it is',
  `uuid` char(36) NOT NULL COMMENT 'unique identification number to browse profile',
  `first_name` varchar(30) DEFAULT ' ' COMMENT 'First name of the user',
  `last_name` varchar(30) NOT NULL DEFAULT ' ' COMMENT 'Last name of the user',
  `profile_pic` varchar(50) DEFAULT NULL,
  `gender` int(1) NOT NULL DEFAULT '1' COMMENT '1=male, 0 = female',
  `date_of_birth` date NOT NULL DEFAULT '1970-00-00' COMMENT 'date of birth (Y-m-d)',
  `address_primary_street` varchar(60) NOT NULL COMMENT 'street 1',
  `address_secondary_street` varchar(60) NOT NULL COMMENT 'street 2',
  `address_city` varchar(60) NOT NULL COMMENT 'city',
  `address_state` varchar(60) DEFAULT NULL COMMENT 'state',
  `address_country` varchar(80) NOT NULL DEFAULT 'USA' COMMENT 'country',
  `address_postal_code` int(5) unsigned zerofill DEFAULT '00000' COMMENT 'postal_code',
  `primary_email_address` varchar(80) NOT NULL DEFAULT '-',
  `secondary_email_address` varchar(80) DEFAULT '-' COMMENT 'extra email address than login email address',
  `primary_phone` varchar(16) DEFAULT 'xxx xxx-xxxx' COMMENT 'phone number',
  `secondary_phone` varchar(16) NOT NULL DEFAULT 'xxx xxx-xxxx' COMMENT 'another phone number',
  `order_count` int(5) NOT NULL DEFAULT '0' COMMENT 'total order',
  `company_count` int(5) NOT NULL DEFAULT '0' COMMENT 'total company',
  `status` int(1) NOT NULL DEFAULT '0' COMMENT 'arbitrary status',
  `created` datetime NOT NULL,
  `modified` datetime NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `user_id` (`user_id`,`uuid`),
  UNIQUE KEY `user_id_3` (`user_id`),
  KEY `user_id_2` (`user_id`),
  CONSTRAINT `profiles_ibfk_1` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8