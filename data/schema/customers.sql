CREATE TABLE `customers` (
  `id` int(8) NOT NULL AUTO_INCREMENT,
  `company_id` int(3) NOT NULL COMMENT 'which company this customer belongs to',
  `name` varchar(40) DEFAULT '-' COMMENT 'name of the customer''s primary contact person',
  `company_name` varchar(60) DEFAULT '-' COMMENT 'company name of the customer',
  `crm_dealer_id` varchar(30) DEFAULT NULL COMMENT 'dealer id of this customer synced from CRM',
  `crm_lead_id` varchar(30) DEFAULT NULL COMMENT 'lead id of this customer synced from CRM',
  `primary_street` varchar(80) DEFAULT NULL COMMENT 'street address first line',
  `secondary_street` varchar(80) DEFAULT '-' COMMENT 'second address first line',
  `city` varchar(30) DEFAULT '-' COMMENT 'city of the customer',
  `state` varchar(30) DEFAULT '-' COMMENT 'state of this customer',
  `postal_code` int(5) unsigned zerofill DEFAULT '00000' COMMENT 'postal code of this customer',
  `country` varchar(40) DEFAULT 'USA' COMMENT 'default country of the customer',
  `billing_primary_street` varchar(80) DEFAULT '-' COMMENT 'billing address street address first',
  `billing_secondary_street` varchar(80) DEFAULT '-' COMMENT 'secondary address street',
  `billing_city` varchar(30) DEFAULT '-' COMMENT 'billing address city',
  `billing_state` varchar(30) DEFAULT '-' COMMENT 'billing address state',
  `billing_postal_code` int(5) unsigned zerofill DEFAULT '00000' COMMENT 'billing address postal code',
  `billing_country` varchar(40) DEFAULT 'USA' COMMENT 'billing country',
  `shipping_primary_street` varchar(80) DEFAULT '-' COMMENT 'shipping primary street address',
  `shipping_secondary_street` varchar(80) DEFAULT '-' COMMENT 'shipping secondary street address',
  `shipping_city` varchar(30) DEFAULT '-' COMMENT 'shipping city',
  `shipping_state` varchar(30) DEFAULT NULL,
  `shipping_postal_code` int(5) unsigned zerofill DEFAULT '00000' COMMENT 'shipping address postal code',
  `shipping_country` varchar(40) DEFAULT 'USA' COMMENT 'shipping country',
  `primary_email_address` varchar(80) DEFAULT NULL COMMENT 'customer primary email address',
  `secondary_email_address` varchar(80) DEFAULT NULL COMMENT 'secondary email address',
  `primary_phone` varchar(16) DEFAULT NULL COMMENT 'primary phone or mobile',
  `secondary_phone` varchar(16) DEFAULT NULL COMMENT 'secondary phone or mobile',
  `order_count` int(6) DEFAULT NULL COMMENT 'total order for this customer',
  `is_lead` tinyint(1) DEFAULT '0' COMMENT 'customer was lead or dealer. find specified ID from the CRM synced customer data',
  `is_dealer` tinyint(1) DEFAULT '0' COMMENT 'customer was lead or dealer. find specified ID from the CRM synced customer data',
  `created` datetime NOT NULL,
  `modified` datetime NOT NULL,
  PRIMARY KEY (`id`),
  KEY `company_id` (`company_id`),
  CONSTRAINT `customers_ibfk_1` FOREIGN KEY (`company_id`) REFERENCES `companies` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8